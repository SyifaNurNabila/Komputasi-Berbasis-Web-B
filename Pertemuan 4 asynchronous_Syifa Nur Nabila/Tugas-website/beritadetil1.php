    <h1>Kesimpulan Sementara KPU-Komisi II DPR RI Jika Pilkada Kotak Kosong Menang</h1>
      <img src="detil1.png" style="display: block; margin-left: auto; margin-right: auto;"/>
        <section>
            
            <p>
              <div class="jumbotron justify">
                <div>Jakarta - Komisi II DPR RI bersama KPU RI melaksanakan rapat dengar pendapat (RDP) terkait pelaksanaan Pilkada jika melawan kotak kosong. Adapun dari hasil kesimpulan sementara, Komisi II menyatakan Pilkada akan diselenggarakan kembali pada 2025 jika kotak kosong menang di suatu wilayah.
                Kesimpulan rapat berjalan alot antara pemerintah dan Komisi II. Rapat yang dipimpin oleh Ketua Komisi II DPR RI Ahmad Doli Kurnia berjalan hingga dini hari.
                Dalam kesimpulan sementara Doli menyebut pelaksanaan Pilkada ulang jika kotak kosong menang berpedoman pada Pasal 54D Undang-Undang Nomor 10 Tahun 2016.<br><br>
                "Daerah yang pelaksanaan Pilkadanya hanya terdiri dari 1 (satu) pasang calon dan tidak mendapatkan suara lebih dari 50%, Komisi II DPR RI, Kementerian Dalam Negeri (Kemendagri), 
                Komisi Pemilihan Umum Republik Indonesia (KPU RI), Badan Pengawas Pemilihan Umum Republik Indonesia (BAWASLU RI) dan Dewan Kehormatan Penyelenggara Pemilihan Umum Republik Indonesia (DKPP RI) secara bersama menyetujui Pemilihan Gubernur dan Wakil Gubernur, Bupati dan Wakil Bupati serta Wali kota dan Wakil Wali kota diselenggarakan kembali pada tahun berikutnya yakni tahun 2025," kata Doli membacakan kesimpulan rapat untuk poin pertama.<br><br>

                Nantinya KPU RI hingga Bawaslu mesti menindaklanjuti hal itu sesuai dengan PKPU Nomor 10 Tahun 2024. Adapun ketetapan pelaksanaan Pilkada ulang akan dibahas kembali oleh pemerintah dan DPR pada 27 September mendatang.
                "Komisi II DPR RI meminta kepada KPU RI dan Bawaslu RI untuk menindaklanjuti permasalahan tersebut sesuai dengan PKPU Nomor 8 Tahun 2024 yang sebagaimana telah diubah dalam ketentuan PKPU Nomor 10 Tahun 2024 tentang Pencalonan Pemilihan Gubernur dan Wakil Gubernur, 
                Bupati dan Wakil Bupati serta Wali kota dan Wakil Wali kota," ujar Doli.<br><br>

                "Nanti kita akan bicara tentang kesimpulan pada saat apakah ini perlu dimasukkan dalam kesimpulan pada saat tanggal 27 di RDP atau tidak," tambahnya.<br>
                Adapun Komisi II menemukan permasalahan pencalonan kepala daerah jelang Pilkada serentak. Setidaknya ada dua kasus yang menjadi atensi Komisi II dan tertuang pada kesimpulan sementara.<br><br>
                "Terhadap daerah yang menghadapi permasalahan pencalonan kepala daerah: a. pasangan calon yang mendaftar pada masa perpanjangan di Pilkada dengan hanya satu pasangan calon yang telah memenuhi ambang batas tidak diterima pendaftarannya oleh KPU Daerah," bunyi poin kesimpulan itu.<br>
                "b. partai politik yang mendaftarkan pasangan calon lebih dari satu kali pada masa pendaftaran dengan pasangan calon yang berbeda tidak diterima oleh KPU Daerah," tambahnya.<br><br>
                Untuk kasus di atas, Komisi II meminta KPU hingga Bawaslu menindaklanjuti sesuai dengan PKPU yang ada. Adapun draf dari PKPU itu akan dibahas pada 27 September 2024.
                "Komisi II DPR RI meminta kepada KPU RI dan Bawaslu RI untuk menindaklanjuti permasalahan tersebut sesuai dengan PKPU nomor 8 tahun 2024 yang sebagaimana telah diubah dalam ketentuan PKPU nomor 10 tahun 2024 tentang pencalonan pemilihan Gubernur dan Wakil Gubernur, Bupati dan Wakil Bupati, serta Wali Kota dan Wakil Wali Kota," tutur Doli.<br>
                "Nanti kita lanjutkan ya tanggal 27 ya draf PKPU-nya," imbuhnya.<br><br>


            <p><div class="jumbotron justify">
                  <div>Melanjutkan studi di luar negeri merupakan impian banyak orang, karena menawarkan kesempatan untuk mendapatkan pengalaman pendidikan dan budaya yang berharga. Banyak cara yang bisa dilakukan untuk bisa ke luar negeri, salah satunya dengan melanjutkan studi. Namun, beberapa kendala membuat seseorang kesulitan untuk berkuliah di sana. 
                    Oleh karena itu, mendapatkan beasiswa bisa menjadi salah satu solusi. Dalam artikel ini, saya akan membagikan beberapa tips yang dapat membantu dalam proses mendapatkan beasiswa untuk studi di luar negeri. 
                    Memahami cara yang tepat untuk melamar beasiswa dapat meningkatkan peluangmu untuk diterima dan memastikan kamu mendapatkan dukungan yang diperlukan untuk menyelesaikan pendidikanmu di luar negeri.
                  <h3>Kelancaran Bahasa Asing</h3>
                    Tentu syarat utama untuk mendapatkan beasiswa luar negeri yakni memiliki kemampuan berbahasa asing, khususnya bahasa Inggris. Bahasa internasional ini akan kamu gunakan untuk berkomunikasi. Oleh karena itu, kamu harus melampirkan sertifikat tes bahasa Inggris seperti TOEFL dengan skor minimal 550 (paper based) dan IELTS dengan skor 6,5-7.
                    “Di Umsida pun ketika ada mahasiswa yang akan mengikuti program ke luar negeri, wajib menyertakan sertifikat tes bahasa Inggris. Misalnya hasil tes Test of English Proficiency (TOEP) dari lembaga bahasa Umsida,” ucap perempuan yang aktif di PWM ini.
                    Selain bahasa Inggris, kamu juga perlu mempelajari bahasa negara yang kamu tuju. Karena tidak dapat dipungkiri jika bahasa tersebutlah yang akan kamu temui di sana. Jadi, jangan lupa belajar juga, ya! <br>
                  <h3>Riset Tentang Program yang Akan Diikuti</h3>
                    “Di LKUI Umsida, mahasiswa yang telah mengikuti program ke luar negeri membuat laporan tentang kegiatan yang mereka lakukan selama di sana. Laporan itu bisa dijadikan sebagai salah satu sumber informasi kepada mahasiswa yang akan mendaftar,” lanjutnya.
                    Kamu juga harus melakukan riset tentang program apa dan negara mana yang akan kamu pilih untuk melanjutkan studi. Hal ini dikarenakan tiap program beasiswa memiliki persyaratan yang berbeda-beda, seperti jenis beasiswa, keperluan yang dibiayai, akomodasi, dan lainnya.<br>
                  <h3>Aktif bertanya ke Pihak Kampus</h3>
                    Selanjutnya, kamu bisa menanyakan hal-hal terkait beasiswa kepada pihak kampus. Beasiswa memang bisa kamu dapatkan secara individu. Tapi, biasanya kampus juga memiliki rekomendasi atau kerjasama dengan universitas asing yang bisa kamu jadikan sebagai referensi.
                    Untuk mendapatkan informasi tersebut, kamu bisa mulai bertanya kepada tingkat yang paling dekat seperti program studi. Biasanya, prodi memiliki kerjasama dengan universitas asing di bidang yang sama, sedangkan informasi dari kampus lebih ke arah umum. Oleh karena itu, kamu bisa mulai dari ranah prodi jika ingin melanjutkan studi di bidang linier.
                    “Mahasiswa bisa bertanya ke pihak prodi untuk menunjukkan keinginannya untuk terlibat di kegiatan internasional. Atau jika sudah memiliki daftar beasiswa atau program studi lanjutan, hal itu bisa dikonsultasikan kepada pihak prodi,” lanjutnya.<br><br>
